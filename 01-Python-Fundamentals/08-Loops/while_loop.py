""" 
While loop - When you don't know how many times you have to repeat the task

- Do this again and again until a condition is false.
"""
# While loop with increasing value
i = 0
while (i < 3):
    print(i)
    i += 1

# While loop with decreasing value
print("")
count = 5
while (count > 0):
    print(count)
    count = count - 1

